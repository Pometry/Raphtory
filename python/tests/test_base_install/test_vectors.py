def post_json(url, payload):
    data = json.dumps(payload).encode()
    req = Request(
        url,
        data=data,
        headers={"Content-Type": "application/json"},
        method="POST",
    )
    try:
        with urlopen(req, timeout=50) as r:
            return r.status, r.read()
    except HTTPError as e:
        return e.code, e.read()


