def test_default_template():
    g = Graph()
    g.add_node(1, "node1")
    g.add_edge(2, "node1", "node1")

    running = constant_embedding.start(7341)

    embeddings = OpenAIEmbeddings(api_base="http://localhost:7341")
    v_cache = VectorCache(embeddings)
    vg = g.vectorise(v_cache)

    node_docs = vg.nodes_by_similarity(query="whatever", limit=10).get_documents()
    assert len(node_docs) == 1
    assert node_docs[0].content == "Node node1 has the following properties:\n"

    edge_docs = vg.edges_by_similarity(query="whatever", limit=10).get_documents()
    assert len(edge_docs) == 1
    assert (
        edge_docs[0].content
        == "There is an edge from node1 to node1 with events at:\n- Jan 1 1970 00:00\n"
    )

    running.stop()
