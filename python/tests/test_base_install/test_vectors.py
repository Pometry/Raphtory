def floats_are_equals(float1: float, float2: float) -> bool:
    return bool(numpy.isclose(float1, float2, rtol=1e-05, atol=1e-08, equal_nan=False))


# the graph generated by this function looks like this:
#
#        edge1
#      ╭─────── node2
#    node1
#      ╰─────── node3  ───── node4
#           edge2             edge3
#
#
def create_graph() -> VectorisedGraph:
    g = Graph()

    g.add_node(1, "node1")
    g.add_node(2, "node2")
    g.add_node(3, "node3")
    g.add_node(4, "node4")

    g.add_edge(2, "node1", "node2", {"name": "edge1"})
    g.add_edge(3, "node1", "node3", {"name": "edge2"})
    g.add_edge(4, "node3", "node4", {"name": "edge3"})

    embeddings = OpenAIEmbeddings(api_base="http://localhost:7340")
    v_cache = VectorCache(embeddings)
    vg = g.vectorise(v_cache, nodes="{{ name }}", edges="{{ properties.name }}")

    return vg


